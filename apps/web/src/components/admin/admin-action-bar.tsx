'use client';
import React, { createContext, useContext, useState, useCallback, useEffect, useRef } from 'react';

export interface AdminAction {
  id: string;
  labelKey: string;
  icon?: React.ReactNode;
  variant?: 'primary' | 'secondary' | 'danger' | 'ghost';
  enabled?: boolean;
  tooltipKey?: string;
  onClick: () => void | Promise<void>;
}

interface AdminActionBarContextValue {
  visible: boolean;
  actions: AdminAction[];
  setActions: (acts: AdminAction[]) => void;
  setVisible: (v: boolean) => void;
  clearActions: () => void;
}

const AdminActionBarContext = createContext<AdminActionBarContextValue | null>(null);

export function useAdminActionBar(): AdminActionBarContextValue {
  const ctx = useContext(AdminActionBarContext);
  if (!ctx) throw new Error('useAdminActionBar must be used within AdminActionBarProvider');
  return ctx;
}

export function useRegisterAdminActions(acts: AdminAction[], visible = true): void {
  const { setActions, setVisible, clearActions } = useAdminActionBar();
  const serializedRef = useRef('');

  useEffect(() => {
    const serialized = JSON.stringify(acts.map(({ id, labelKey, variant, enabled }) => ({ id, labelKey, variant, enabled: enabled ?? true })));
    if (serializedRef.current !== serialized) {
      setActions(acts);
      setVisible(visible);
      serializedRef.current = serialized;
    }
  });

  useEffect(() => {
    return () => {
      clearActions();
    };
  }, []);
}

export function useStableHandlers<T extends Record<string, (...args: any[]) => any>>(handlers: T): { exec: (id: string) => void } {
  const ref = useRef(handlers);
  ref.current = handlers;
  const exec = useCallback((actionId: string, ...args: any[]) => {
    const fn = ref.current[actionId];
    if (fn) fn(...args);
  }, []);
  return { exec };
}

export function AdminActionBarProvider({ children }: { children: React.ReactNode }) {
  const [visible, setVisible] = useState(false);
  const [actions, setActions] = useState<AdminAction[]>([]);

  const clearActions = useCallback(() => {
    setActions([]);
    setVisible(false);
  }, []);

  return (
    <AdminActionBarContext.Provider value={{ visible, actions, setActions, setVisible, clearActions }}>
      {children}
    </AdminActionBarContext.Provider>
  );
}

